    private void jButtonFinalizarActionPerformed(ActionEvent evt) {//GEN-FIRST:event_jButtonFinalizarActionPerformed
        DAO dao = new DAO();
        try {
            if(Double.parseDouble(jTextFieldQuantidade.getText()) <=0){
                throw new NumberFormatException();
            }
            if (!jTextFieldQuantidade.getText().isEmpty()) {
                dao.realizaVenda(this, (String) jComboBoxCliente.getSelectedItem(), (String) jComboBoxBebida.getSelectedItem(), Integer.parseInt(jTextFieldQuantidade.getText()));
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Quantidade invÃ¡lida", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonFinalizarActionPerformed


